public class Variables {

  public static void main(String[] args) {
    System.err.println("We will learn variables in this lecture");
    //  type variableName = value;
    String firstName = "ahnaf";
    String lastName = "mottaki";
    int age = 20;
    float price = 56.43f;
    double salary = 50000.0;
    char gender = 'M';
    boolean isMarried = false;
    // declaring multiple variables
    String first_name = "ahnaf",
      last_name = "mottaki";
    // one value to multiple variables
    int x, y, z;
    x = y = z = 10;

    // final variables
    final String NAME = "ahnaf";
    final int AGE = 20;
    final double SALARY = 50000.0;
    final char GENDER = 'M';
    final boolean IS_MARRIED = false;
  }
}
