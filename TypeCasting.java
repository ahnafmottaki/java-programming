public class TypeCasting {

  public static void main(String[] args) {
    System.out.println("In this lecture, we will learn type casting in java");
    int maxScore = 500;
    int userScore = 423;
    double percentage = ((double) userScore / maxScore) * 100.0d;
    System.out.print("score percentage is: ");
    System.out.println(percentage);
    System.out.print(45 % 21);
  }
}
